/**
 * @brief Initialize UART for RS232 communication
 */
void UART_Init(void) {
    // Start UART receive in interrupt mode
    HAL_UART_Receive_IT(&huart1, &uartRxData, 1);

    // Send welcome message
    UART_SendResponse("Robotic Arm Controller Ready\r\n");
}

/**
 * @brief Send string over UART
 * @param message String to send
 */
void UART_SendResponse(const char* message) {
    HAL_UART_Transmit(&huart1, (uint8_t*)message, strlen(message), 100);
}

/**
 * @brief Send periodic heartbeat to base system
 */
void UART_SendHeartbeat(void) {
    uint32_t currentTime = HAL_GetTick();

    if (currentTime - lastHeartbeatTime >= HEARTBEAT_INTERVAL) {
        lastHeartbeatTime = currentTime;

        // Format: HB;RA:xx.xx;PP:xxx.xx;PEN:x;STAT:x
        sprintf((char*)uartTxBuffer,
                "HB;RA:%.2f;PP:%.2f;PEN:%d;STAT:%d\r\n",
                robotState.revoluteAngle,
                robotState.prismaticPosition,
                robotState.penPosition,
                robotState.systemStatus);

        HAL_UART_Transmit(&huart1, uartTxBuffer, strlen((char*)uartTxBuffer), 100);
    }
}

/**
 * @brief Process received command
 */
void UART_ProcessCommand(void) {
    char cmdBuffer[UART_RX_BUFFER_SIZE];
    strcpy(cmdBuffer, (char*)uartRxBuffer);

    // Split command by delimiter
    char* token = strtok(cmdBuffer, ";");

    if (token != NULL) {
        // Handle different command types
        if (strcmp(token, "MOV") == 0) {
            // MOV;RA:xx.xx;PP:xxx.xx - Move to specified position
            float ra = 0.0f, pp = 0.0f;
            token = strtok(NULL, ";");

            while (token != NULL) {
                if (strncmp(token, "RA:", 3) == 0) {
                    ra = atof(token + 3);
                } else if (strncmp(token, "PP:", 3) == 0) {
                    pp = atof(token + 3);
                }
                token = strtok(NULL, ";");
            }

            // Update target positions
            robotState.revoluteAngle = ra;
            robotState.prismaticPosition = pp;

            sprintf((char*)uartTxBuffer, "ACK;MOV;RA:%.2f;PP:%.2f\r\n", ra, pp);
            HAL_UART_Transmit(&huart1, uartTxBuffer, strlen((char*)uartTxBuffer), 100);
        }
        else if (strcmp(token, "PEN") == 0) {
            // PEN;x - Control pen (0: up, 1: down)
            token = strtok(NULL, ";");
            if (token != NULL) {
                int pen = atoi(token);
                robotState.penPosition = (pen > 0) ? 1 : 0;

                sprintf((char*)uartTxBuffer, "ACK;PEN;%d\r\n", robotState.penPosition);
                HAL_UART_Transmit(&huart1, uartTxBuffer, strlen((char*)uartTxBuffer), 100);
            }
        }
        else if (strcmp(token, "HOME") == 0) {
            // HOME - Move to home position
            robotState.revoluteAngle = 0.0f;
            robotState.prismaticPosition = 100.0f;

            sprintf((char*)uartTxBuffer, "ACK;HOME\r\n");
            HAL_UART_Transmit(&huart1, uartTxBuffer, strlen((char*)uartTxBuffer), 100);
        }
        else if (strcmp(token, "STAT") == 0) {
            // STAT - Request status
            sprintf((char*)uartTxBuffer,
                    "STAT;RA:%.2f;PP:%.2f;PEN:%d;SYS:%d\r\n",
                    robotState.revoluteAngle,
                    robotState.prismaticPosition,
                    robotState.penPosition,
                    robotState.systemStatus);
            HAL_UART_Transmit(&huart1, uartTxBuffer, strlen((char*)uartTxBuffer), 100);
        }
        else {
            // Unknown command
            sprintf((char*)uartTxBuffer, "ERR;UNKNOWN_CMD\r\n");
            HAL_UART_Transmit(&huart1, uartTxBuffer, strlen((char*)uartTxBuffer), 100);
        }
    }
}
