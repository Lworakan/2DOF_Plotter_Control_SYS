/**
 * @brief Read encoder counter values
 */
void ReadEncoders(void) {
    // Read encoder 1 (Revolute) - TIM2
    encoderCount1 = (int32_t)__HAL_TIM_GET_COUNTER(&htim2);

    // Read encoder 2 (Prismatic) - TIM3
    encoderCount2 = (int32_t)__HAL_TIM_GET_COUNTER(&htim3);

    // Calculate positions in real-world units
    CalculatePositions();
}

/**
 * @brief Calculate positions in real-world units (degrees and mm)
 */
void CalculatePositions(void) {
    // Calculate revolute angle in degrees
    // Formula: angle = (count / CPR) * 360Â° * gear_ratio
    revoluteAngleDeg = (float)encoderCount1 / REVOLUTE_ENCODER_CPR * 360.0f * REVOLUTE_GEAR_RATIO;

    // Calculate prismatic position in millimeters
    // Formula: position = (count / CPR) * mm_per_rev * gear_ratio
    prismaticPositionMm = (float)encoderCount2 / PRISMATIC_ENCODER_CPR * PRISMATIC_MM_PER_REV * PRISMATIC_GEAR_RATIO;
}

/**
 * @brief Calculate encoder speeds
 */
void CalculateSpeed(void) {
    uint32_t currentTime = HAL_GetTick();
    uint32_t timeDiff = currentTime - lastSpeedCalcTime;

    if (timeDiff >= 50) { // Calculate speed every 50ms
        // Calculate speed as count difference per second
        encoderSpeed1 = (int16_t)((encoderCount1 - lastEncoderCount1) * 1000 / timeDiff);
        encoderSpeed2 = (int16_t)((encoderCount2 - lastEncoderCount2) * 1000 / timeDiff);

        // Update last values
        lastEncoderCount1 = encoderCount1;
        lastEncoderCount2 = encoderCount2;
        lastSpeedCalcTime = currentTime;
    }
}

/**
 * @brief Send encoder data over UART
 */
void SendEncoderData(void) {
    uint32_t currentTime = HAL_GetTick();

    if (currentTime - lastSendTime >= SEND_INTERVAL) {
        lastSendTime = currentTime;

        // Format the message without using float formatting
        int32_t revolute_angle_int = (int32_t)(revoluteAngleDeg * 10); // Convert to fixed-point (1 decimal place)
        int32_t prismatic_pos_int = (int32_t)(prismaticPositionMm * 10); // Convert to fixed-point (1 decimal place)

        // Format: ENC;RAW_REV:xxxxx;RAW_PRI:xxxxx;REV:xxx.x;PRI:xxx.x;REV_SPD:xxx;PRI_SPD:xxx
        sprintf((char*)uartTxBuffer,
                "ENC;RAW_REV:%ld;RAW_PRI:%ld;REV:%ld.%01ld;PRI:%ld.%01ld;REV_SPD:%d;PRI_SPD:%d\r\n",
                encoderCount1, encoderCount2,
                revolute_angle_int / 10, revolute_angle_int % 10,
                prismatic_pos_int / 10, prismatic_pos_int % 10,
                encoderSpeed1, encoderSpeed2);

        // Send via UART
        HAL_UART_Transmit(&huart1, uartTxBuffer, strlen((char*)uartTxBuffer), 100);

        // Toggle the LED to indicate activity
        HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_1);
    }
}

